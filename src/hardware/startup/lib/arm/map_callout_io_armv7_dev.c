uintptr_t
callout_io_map_armv7_dev(unsigned size, paddr_t *phys)
{
	/*
	 * Map using Device attribute
	 */
	return mmu_map(~0, *phys, size, PROT_READ|PROT_WRITE|PROT_DEVICE);
}

